/**
 * Convert Motoko Time (bigint nanoseconds) to a JS Date.
 */
export function timeToDate(timestamp: bigint): Date {
  return new Date(Number(timestamp / 1_000_000n));
}

/**
 * Format a timestamp as a relative time string (e.g., "2 hours ago").
 */
export function formatRelativeTime(timestamp: bigint): string {
  const date = timeToDate(timestamp);
  const now = new Date();
  const diffMs = now.getTime() - date.getTime();
  const diffSecs = Math.floor(diffMs / 1000);
  const diffMins = Math.floor(diffSecs / 60);
  const diffHours = Math.floor(diffMins / 60);
  const diffDays = Math.floor(diffHours / 24);
  const diffWeeks = Math.floor(diffDays / 7);

  if (diffSecs < 60) return "just now";
  if (diffMins < 60) return `${diffMins}m ago`;
  if (diffHours < 24) return `${diffHours}h ago`;
  if (diffDays < 7) return `${diffDays}d ago`;
  if (diffWeeks < 4) return `${diffWeeks}w ago`;

  return date.toLocaleDateString("en-US", {
    month: "short",
    day: "numeric",
    year: date.getFullYear() !== now.getFullYear() ? "numeric" : undefined,
  });
}

/**
 * Format a timestamp as a full date string.
 */
export function formatFullDate(timestamp: bigint): string {
  const date = timeToDate(timestamp);
  return date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });
}
